/* ===== 工具函数 ===== */

export function getChapterQuiz(ch: number): QuizQuestion[] {
  return chapterQuiz[ch] || []
}

export function getAllQuestions(): QuizQuestion[] {
  return Object.values(chapterQuiz).flat()
}

export function randomQuiz(n: number, fromChapters?: number[]): QuizQuestion[] {
  const pool = fromChapters
    ? Object.entries(chapterQuiz)
        .filter(([k]) => fromChapters.includes(Number(k)))
        .flatMap(([, qs]) => qs)
    : getAllQuestions()
  const shuffled = [...pool].sort(() => Math.random() - 0.5)
  return shuffled.slice(0, n)
}

